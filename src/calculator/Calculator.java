public class Calculator {
    static int rezulat =0;
    public static int aduna(int a, int b){
    rezultat=a+b;
    return rezultat;
    }
    
    public static int scade(int a, int b){
    rezultat=a-b;
    return rezultat;
    }
    
     public static int inmulteste(int a, int b){
    rezultat=a*b;
    return rezultat;
    }
     public static int imparte(int a, int b){
    rezultat=a/b;
    return rezultat;
    }
}
